#include<iostream>

using namespace std;

/*
 * 运算符
 * 和java运算一样
*/
int main() {
    /*
    加减乘除
    */
    int a = 10;
    int b = 3;
    cout << "加" << a + b << endl;
    cout << "减" << a - b << endl;
    cout << "乘" << a * b << endl;
    cout << "除" << a / b << endl;
    cout << "取模" << a % b << endl;

    /*
    和java运算一样：++a  a++  =  +=  -= *=  /=  ==  ！=  <   >   <=   >=   !   &&  ||
    */

    system("pause");
    return 0;
}